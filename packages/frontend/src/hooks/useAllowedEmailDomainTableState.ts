export function useAllowedEmailDomainTableState(
  options: UseAllowedEmailDomainTableStateOptions = {}
): UseAllowedEmailDomainTableStateResult {
  const {
    initialPageSize = 20,
    enableSelection = false,
    initialSort,
    searchDebounceMs = 300,
    searchFields = ['domain', 'description'],
    onRowClick,
  } = options;

  // Composable hooks
  const pagination = usePagination({
    initialPageSize,
  });

  const selection = useSelection<AllowedEmailDomain>({
    keyExtractor: (item) => item.id,
  });

  const sorting = useSorting<AllowedEmailDomainSortField>({
    initialField: initialSort?.field,
    initialDirection: initialSort?.direction,
  });

  const search = useSearch({
    debounceMs: searchDebounceMs,
  });

  // Data fetching
  const {
    data: rawData,
    loading,
    error,
    totalCount: rawTotalCount,
    refetch,
  } = useAllowedEmailDomainList({
    page: pagination.page,
    pageSize: pagination.pageSize,
  });

  const { remove } = useAllowedEmailDomainMutations();

  // Update pagination total when data changes
  useEffect(() => {
    pagination.setTotalItems(rawTotalCount);
  }, [rawTotalCount, pagination.setTotalItems]);

  // Apply client-side search if query exists
  // (ideally this would be server-side, but this provides a fallback)
  const data = useMemo(() => {
    if (!search.debouncedQuery || searchFields.length === 0) {
      return rawData;
    }

    const query = search.debouncedQuery.toLowerCase();
    return rawData.filter((item) =>
      searchFields.some((field) => {
        const value = item[field as keyof AllowedEmailDomain];
        if (value === null || value === undefined) return false;
        return String(value).toLowerCase().includes(query);
      })
    );
  }, [rawData, search.debouncedQuery, searchFields]);

  // Computed state
  const totalCount = search.debouncedQuery ? data.length : rawTotalCount;
  const isEmpty = data.length === 0 && !loading;

  // Actions
  const handleRowClick = useCallback(
    (item: AllowedEmailDomain) => {
      onRowClick?.(item);
    },
    [onRowClick]
  );

  const deleteItem = useCallback(
    async (id: number): Promise<boolean> => {
      const success = await remove(id);
      if (success) {
        refetch();
      }
      return success;
    },
    [remove, refetch]
  );

  const deleteSelected = useCallback(async () => {
    const ids = selection.selected.map((item) => item.id);
    for (const id of ids) {
      await remove(id);
    }
    selection.deselectAll();
    refetch();
  }, [selection, remove, refetch]);

  const reset = useCallback(() => {
    pagination.reset();
    selection.reset();
    sorting.reset();
    search.reset();
  }, [pagination, selection, sorting, search]);

  // Build state object
  const state: AllowedEmailDomainTableState = {
    data,
    loading,
    error,
    totalCount,
    isEmpty,
    pagination,
    selection,
    sorting,
    search,
  };

  // Build actions object
  const actions: AllowedEmailDomainTableActions = {
    refetch,
    handleRowClick,
    deleteItem,
    deleteSelected,
    reset,
  };

  return {
    ...state,
    ...actions,
  };
}

